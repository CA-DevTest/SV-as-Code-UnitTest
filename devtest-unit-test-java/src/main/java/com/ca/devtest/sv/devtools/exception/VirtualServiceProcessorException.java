package com.ca.devtest.sv.devtools.exception;

/**
 * @author gaspa03
 *
 */
public class VirtualServiceProcessorException extends Exception {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = -2994095405066794649L;


	public VirtualServiceProcessorException() {
		super();
	}

	public VirtualServiceProcessorException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public VirtualServiceProcessorException(String message, Throwable cause) {
		super(message, cause);
	}

	public VirtualServiceProcessorException(String message) {
		super(message);
	}

	public VirtualServiceProcessorException(Throwable cause) {
		super(cause);
	}

	

}
